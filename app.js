function fmt(t) {
  return t.replace(/&/g, '&amp;').replace(/</g, '&lt;').replace(/>/g, '&gt;')
    .replace(/\*\*(.*?)\*\*/g, '<strong>$1</strong>')
    .replace(/\n(\d+)\. /g, '<br><strong>$1.</strong> ')
    .replace(/\n- /g, '<br>&bull; ')
    .replace(/\n/g, '<br>');
}

